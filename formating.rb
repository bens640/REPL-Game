module Formatting

  def say(person, message)
    puts(person.nil? ? message : "#{person.capitalize}: #{message}")
  end

  def ask_question(question, options)
    puts question, options
    gets.chomp.downcase
  end

  def put_a_line
    puts "*" * 100
  end



  def first_suggest(money, strength)
    if money < 2 && strength > 3
      puts "You dont have that much money, think about taking a walk "
    elsif money <= 1
      puts "You dont have much money, think about sitting down"
    else
      puts "You are feeling fine"
    end
  end


  def clear_screen
  puts `clear`
  end

  def text_flasher(text)
    3.times do
      print "\r#{text}"
      sleep(0.5)
      print "\r#{ ' ' * text.size}"
      sleep(0.5)
    end
  end

  def stat_box(health, attack_power,turn)

      puts <<box
  -----------                ------------------                 ----------
 |health : #{health}|               | Attack Power : #{attack_power}|                | Turn: #{turn}|
  -----------                ------------------                 ----------
box
    end

